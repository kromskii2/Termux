def расшифровать_марку_стали(марка):
    # Проверяем, является ли марка углеродистой или легированной
    if any(c.isalpha() for c in марка):
        углеродистая = False
    else:
        углеродистая = True

    # Проверяем, является ли марка конструкционной
    if марка[0].isdigit():
        конструкционная = True
    elif марка.startswith('У'):
        конструкционная = False
    else:
        конструкционная = False

    # Проверяем, является ли марка инструментальной
    if марка.startswith('У') or марка[0].isdigit():
        инструментальная = False
    else:
        инструментальная = True

    # Проверяем качество стали
    if марка.endswith('А'):
        качество = 'Высококачественная'
    elif марка[-1].isdigit():
        качество = 'Качественная'
    else:
        качество = 'Качественная'

    return {
        'марка': марка,
        'углеродистая': углеродистая,
        'конструкционная': конструкционная,
        'инструментальная': инструментальная,
        'качество': качество
    }


# Пример использования
марка_стали = input("Введите марку стали: ")
результат = расшифровать_марку_стали(марка_стали)
print("Марка стали:", результат['марка'])
print("Углеродистая:", результат['углеродистая'])
print("Конструкционная:", результат['конструкционная'])
print("Инструментальная:", результат['инструментальная'])
print("Качество:", результат['качество'])
